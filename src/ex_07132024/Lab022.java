package ex_07132024;

public class Lab022 {
    public static void main(String[] args) {
        // Compound Assignment Operators
        int a = 10;
        // += , -=, /=, *= . %=
        // += ->
        int b = a+=22; // a = a+10
        System.out.println(b);
        int c = a-=20; // a = a-10
        System.out.println(c);
        int d = a/=5; // a = a/10
        System.out.println(d);
        int e = a*=10; // a = a*10
        System.out.println(e);

    }
}
