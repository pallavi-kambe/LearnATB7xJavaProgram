package ex_07132024;

public class Lab018 {
    public static void main(String[] args) {
        // Escape Chars
        char c = '\n'; // New line / Next Line
        char c1 = '\t'; // Add Tab
        char c2 = '\b'; // Backlash, delete 1 char
        char c3 = '\r'; // Backlash, delete 1 word
        System.out.println("Pallavi" + c3 + "Kambe");
        System.out.println("Pallavi" + c2 + "Kambe");
        System.out.println("Pallavi" + c1 + "Kambe");
        System.out.println("Pallavi" + c + "Kambe");
    }
}
