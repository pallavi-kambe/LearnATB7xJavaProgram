public class Lab073 {
    public static void main(String[] args) {
        for(int i=1;i<=10;i++)
        {
            if(i==1){
                System.out.println("ONE");
            }
            else if(i==2){
                System.out.println("TWO");
            }
            else if(i==3){
                System.out.println("Three");}
            else{
                System.out.println(i);
            }
        }
    }
}
