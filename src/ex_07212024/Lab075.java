package ex_07212024;

public class Lab075 {
    public static void main(String[] args) {
        System.out.println("Even and Odd");
        for(int i=0;i<=10;i++){
            if(i%2==0){
                System.out.println("Even " +i);
            }
            else if(i%2!=0){
                System.out.println(i+ " Odd ");
            }
        }
    }
}
